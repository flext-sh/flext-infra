"""Centralized constants for the workspace subpackage."""

from __future__ import annotations

from enum import StrEnum, unique
from typing import TYPE_CHECKING, Final

if TYPE_CHECKING:
    from flext_infra import t


class FlextInfraWorkspaceConstants:
    """Workspace infrastructure constants."""

    @unique
    class WorkspaceMode(StrEnum):
        """Workspace execution mode enumeration."""

        WORKSPACE = "workspace"
        STANDALONE = "standalone"

    MAKEFILE_REPLACEMENTS: Final[t.Infra.VariadicTuple[t.Infra.StrPair]] = (
        (
            'python3 "$(BASE_MK_DIR)/scripts/mode.py"',
            "python -m flext_infra workspace detect",
        ),
        (
            'python3 "$(WORKSPACE_ROOT)/scripts/sync.py"',
            "python -m flext_infra workspace sync --apply",
        ),
        (
            'python3 "$(WORKSPACE_ROOT)/scripts/dependencies/sync_internal_deps.py"',
            "python -m flext_infra deps internal-sync",
        ),
        (
            'python "$(WORKSPACE_ROOT)/scripts/check/fix_pyrefly_config.py"',
            "python -m flext_infra check fix-pyrefly-config",
        ),
        (
            'python "$(WORKSPACE_ROOT)/scripts/check/workspace_check.py"',
            "python -m flext_infra check run",
        ),
        (
            '$(VENV_PYTHON) "$(BASE_MK_DIR)/scripts/core/pytest_diag_extract.py"',
            "$(VENV_PYTHON) -m flext_infra validate pytest-diag",
        ),
        (
            'python3 "$(WORKSPACE_ROOT)/scripts/github/pr_manager.py"',
            "python3 -m flext_infra github pr",
        ),
    )
    GITIGNORE_REMOVE_EXACT: Final[frozenset[str]] = frozenset({
        "!scripts/",
        "!scripts/**",
        "scripts/",
        "/scripts/",
    })
    REQUIRED_GITIGNORE_ENTRIES: Final[t.StrSequence] = [
        ".reports/",
        ".venv/",
        "__pycache__/",
        "base.mk",
    ]

    MAKEFILE_INCLUDE_OLD: Final[str] = (
        'ifneq ("$(wildcard ../base.mk)", "")\n'
        "include ../base.mk\n"
        "else\n"
        "include base.mk\n"
        "endif"
    )

    MAKEFILE_BOOTSTRAP_TEMPLATE: Final[str] = "makefile_bootstrap.mk.j2"

    MAKEFILE_INCLUDE_BOOTSTRAP: Final[str] = (
        'ifneq ("$(wildcard ../base.mk)", "")\n'
        "include ../base.mk\n"
        "else\n"
        "# === STANDALONE BOOTSTRAP (auto-generates base.mk via flext-infra) ===\n"
        "-include base.mk\n"
        "\n"
        "base.mk: Makefile\n"
        "\t@if python3 -c 'import flext_infra' 2>/dev/null; then \\\n"
        "\t\tpython3 -m flext_infra basemk generate"
        " --project-name $(PROJECT_NAME) --output $@; \\\n"
        "\telif [ -d .venv ] && .venv/bin/python"
        " -c 'import flext_infra' 2>/dev/null; then \\\n"
        "\t\t.venv/bin/python -m flext_infra basemk generate"
        " --project-name $(PROJECT_NAME) --output $@; \\\n"
        "\telse \\\n"
        '\t\techo "==> base.mk not found.'
        ' Bootstrapping standalone environment..."; \\\n'
        "\t\tpython3 -m venv .venv; \\\n"
        "\t\t.venv/bin/pip install -q -U pip flext-infra; \\\n"
        "\t\t.venv/bin/python -m flext_infra basemk generate"
        " --project-name $(PROJECT_NAME) --output $@; \\\n"
        "\tfi\n"
        "\t@test -s $@ || { rm -f $@; exit 1; }\n"
        "\n"
        ".PHONY: venv\n"
        "venv: ## Create standalone virtual environment\n"
        "\t@python3 -m venv .venv\n"
        "\t@.venv/bin/pip install -U pip\n"
        '\t@echo "Virtual environment created at .venv"\n'
        "endif"
    )

    class Makefile:
        """Constants for workspace root Makefile generation."""

        TEMPLATE_NAME: Final[str] = "workspace_makefile.mk.j2"
        "Jinja2 template filename for workspace Makefile generation."
        GENERATED_MARKER: Final[str] = "# @generated by: flext_infra workspace sync"
        "Comment marker inserted into generated Makefiles."
        CUSTOM_INCLUDE: Final[str] = "-include workspace_custom.mk"
        "Makefile include for user customization overrides."


__all__ = ["FlextInfraWorkspaceConstants"]
